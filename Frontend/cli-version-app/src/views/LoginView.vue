<template>
    <div class="container">
    <h2>Login</h2>
    <form @submit.prevent="handleLogin" ref="loginForm">
      <div class="form-group">
        <label for="email">Email</label>
        <input type="email" id="email" name="email" required v-model="email">
      </div>
      <div class="form-group">
        <label for="password">Password</label>
        <input type="password" id="password" name="password" required v-model="password">
      </div>
      <div class="form-group">
        <button type="submit">Login</button>
      </div>
    </form>
    <div class="signup-link">
      Don't have an account? <router-link to="/signup">Sign up</router-link>
    </div>
  </div>
</template>

<script>
import axios from "../libs/axios";
//import Cookies from "js-cookie";
// import SignupView from './SignupView'
  export default {
    name : 'LoginView',
    components :{
      // SignupView
    },
  data (){
    return {
      email: '',
      password:''
    };
  },
  methods: {
    handleLogin() {
      
     const user = {
        email: this.email,
        password: this.password
      }
    
    // Send a POST request to the login endpoint   
       axios.post("auth/login", user)
          
        .then((response) => {            
          if (response.status === 200) {
          // Login successful, redirect to the home page

             // After successful login, retrieve the user data from the response
             const userData = response.data.user;   
             console.log(userData);

             // Save user data to Vue instance or Vuex store           
              this.$store.commit('setUserData', userData);

              // Save user data to local storage                          
              //localStorage.setItem('userData', userData);

              // Save the authentication token in a cookie
              //Cookies.set('authToken', response.data.user.token, { expires: 1 }); // Adjust the expiration as needed
             // Redirect to home page   
                 this.$router.push('/');     
                 
              
          }else {
          // Handle login error (e.g., invalid credentials)
          console.log('Invalid credentials. Please try again.');          
          }        
        })
        //console.log(user); 
        .catch((error) => {
         // Handle any error that occurred during login
            console.error(error);
        });
    }
  }
} 

</script>

